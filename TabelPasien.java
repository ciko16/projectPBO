    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        ktp.setEditable(false);
        
        int baris=jTable1.rowAtPoint(evt.getPoint());
        String nama=jTable1.getValueAt(baris, 1).toString();
        jTextField1.setText(nama);
        String alamat=jTable1.getValueAt(baris, 2).toString();
        jTextField3.setText(alamat);
        String tempat_lahir=jTable1.getValueAt(baris, 3).toString();
        jTextField4.setText(tempat_lahir);
        String tanggal_lahir=jTable1.getValueAt(baris, 4).toString();
        jTextField5.setText(tanggal_lahir);
        String kelamin=jTable1.getValueAt(baris, 5).toString();
        jTextField6.setText(kelamin);
        String no_ktp=jTable1.getValueAt(baris, 6).toString();
        ktp.setText(no_ktp);
        String no_hp=jTable1.getValueAt(baris, 7).toString();
        jTextField7.setText(no_hp);
    }//GEN-LAST:event_jTable1MouseClicked

