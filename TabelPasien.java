    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        try {
            String sql ="UPDATE pasien SET nama = '"+jTextField1.getText()+"',"
                    +"alamat='"+jTextField3.getText()+"',"
                    +"tempat_lahir='"+jTextField4.getText()+"',"
                    +"tanggal_lahir='"+jTextField5.getText()+"',"
                    +"kelamin='"+jTextField6.getText()+"',"
                    +"no_hp='"+jTextField7.getText()+","
                    
                    
                    +"' WHERE no_ktp ='"+ktp.getText()+"'";
            java.sql.Connection conn=(Connection)konfigurasi.configDB();
            java.sql.PreparedStatement pstm=conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil di edit");
            TampilData();
            KosongForm();
        } catch (Exception e){
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_editActionPerformed

